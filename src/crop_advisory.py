def get_advisory(crop, county):
    if crop not in CROP_ADVISORY:
        return None
    data = CROP_ADVISORY[crop]
    varieties = data["varieties"].get(county, data["varieties"]["default"])
    return {
        "varieties"   : varieties,
        "fertiliser"  : data["fertiliser"],
        "herbicides"  : data["herbicides"],
        "spacing"     : data["spacing"],
        "planting_depth": data["planting_depth"],
    }